package algoPrj.day12.인터페이스;

public class 윤계산기클래스 {

	public int add윤(int su1, int su2) {
		return su1+su2;
	}
	
	public int sub윤(int su1, int su2) {
		return su1-su2;
	}
	
	public int multi윤(int su1, int su2) {
		return su1*su2;
	}
	
	public int divide윤(int su1, int su2) {
		return su1/su2;
	}
	

}
