package algoPrj.day8;

public class Ex09 {

	public static void main(String[] args) {
		
		// 지역변수 : 함수 안에서 만든 변수는 그 함수 안에서 유효하다.
		// 변수의 유효범위
		// main에서 만든 변수는 main에서만 유효함
		String[] days=new String[3];	// 인자정보, 반환 정보 활용
		int i=0;	// 배열의 index
		days[i]="놀러가기";
		
		System.out.println(days[i]);
	


	
	
	
	
	}

}
